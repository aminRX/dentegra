    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int row = jTable1.getSelectedRow();
            Menu.currentPaciente = jTable1.getModel().getValueAt(row, 0).toString();
            Menu.currentNombre = jTable1.getModel().getValueAt(row, 1).toString();
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String buscar = txtBuscar.getText();
        if (!buscar.equals("")) {
            String paciente[] = dentegraService.findPacienteModelo(buscar);
            model.setRowCount(0);
            model.addRow(paciente);
            txtBuscar.setText("");
        } else {
            String paciente[][] = dentegraService.pacientesExpedienteYNombre();
            model = new DefaultTableModel(paciente, head);
            jTable1.setModel(model);
        }


    }//GEN-LAST:event_btnBuscarActionPerformed

