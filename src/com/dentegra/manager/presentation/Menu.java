    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        String current = Menu.currentPaciente;
        if (current != null) {
            VerPaciente verPaciente = new VerPaciente();
            verPaciente.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Primero seleccione un paciente");
        }

    }//GEN-LAST:event_jMenuItem3ActionPerformed

    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        String current = Menu.currentPaciente;
        if (current != null) {
            dentegraService.deletePaciente(current);
            JOptionPane.showMessageDialog(null, "Paciente eliminado");
        } else {
            JOptionPane.showMessageDialog(null, "Primero seleccione un paciente a eliminar");
        }

        String paciente[][] = dentegraService.pacientesExpedienteYNombre();
        model = new DefaultTableModel(paciente, head);
        jTable1.setModel(model);
    }//GEN-LAST:event_jMenuItem4ActionPerformed
